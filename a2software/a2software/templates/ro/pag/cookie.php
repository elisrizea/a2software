navigatorului tău şi memorate în computerul, telefonul tău mobil sau alt
dispozitiv, atunci când vizitezi un site. Acest fişier cookie trimite
informaţii înapoi către site ori de câte ori îl revizitezi.</span></p>

<p class=MsoNormal><span style='font-family:"Times New Roman",serif'>Cookie-urile
pot fi&nbsp;<b>permanente</b>&nbsp;(cunoscute drept cookie-uri persistente),
care rămân în computerul tău până când le ştergi, sau&nbsp;<b>temporare</b>&nbsp;(cunoscute
drept cookie-uri pentru sesiune) care sunt valabile doar până când închizi
fereastra navigatorului tău. Cookie-urile pot fi ale părţii-gazdă
(first-party), care sunt configurate de site-ul pe care îl vizitezi, sau ale
unor părţi terţe, care sunt configurate de un site diferit de cel pe care îl
vizitezi.</span></p>

